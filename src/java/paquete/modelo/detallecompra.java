package paquete.modelo;

/**
 *
 * @author Elvis
 */
public class detallecompra {

    String cantidad_com;
    String precio_com;
    String iva_com;
    String subtotal_com;

    public String getCantidad_com() {
        return cantidad_com;
    }

    public void setCantidad_com(String cantidad_com) {
        this.cantidad_com = cantidad_com;
    }

    public String getPrecio_com() {
        return precio_com;
    }

    public void setPrecio_com(String precio_com) {
        this.precio_com = precio_com;
    }

    public String getIva_com() {
        return iva_com;
    }

    public void setIva_com(String iva_com) {
        this.iva_com = iva_com;
    }

    public String getSubtotal_com() {
        return subtotal_com;
    }

    public void setSubtotal_com(String subtotal_com) {
        this.subtotal_com = subtotal_com;
    }

}
