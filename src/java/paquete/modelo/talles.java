public class talles {

    String id_talle;
    String nombre_t;
    String descripcion_t;

    public String getId_talle() {
        return id_talle;
    }

    public void setId_talle(String id_talle) {
        this.id_talle = id_talle;
    }

    public String getNombre_t() {
        return nombre_t;
    }

    public void setNombre_t(String nombre_t) {
        this.nombre_t = nombre_t;
    }

    public String getDescripcion_t() {
        return descripcion_t;
    }

    public void setDescripcion_t(String descripcion_t) {
        this.descripcion_t = descripcion_t;
    }

}
