package paquete.modelo;

/**
 *
 * @author Elvis
 */
public class compra {

    String id_compra;
    String timbrado_com;
    String nrofactura_com;
    String fecha_com;
    String total_com;

    public String getId_compra() {
        return id_compra;
    }

    public void setId_compra(String id_compra) {
        this.id_compra = id_compra;
    }

    public String getTimbrado_com() {
        return timbrado_com;
    }

    public void setTimbrado_com(String timbrado_com) {
        this.timbrado_com = timbrado_com;
    }

    public String getNrofactura_com() {
        return nrofactura_com;
    }

    public void setNrofactura_com(String nrofactura_com) {
        this.nrofactura_com = nrofactura_com;
    }

    public String getFecha_com() {
        return fecha_com;
    }

    public void setFecha_com(String fecha_com) {
        this.fecha_com = fecha_com;
    }

    public String getTotal_com() {
        return total_com;
    }

    public void setTotal_com(String total_com) {
        this.total_com = total_com;
    }

}
