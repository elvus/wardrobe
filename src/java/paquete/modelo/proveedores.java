package paquete.modelo;

/**
 *
 * @author Elvis
 */
public class proveedores extends ciudades {

    String id_proveedor;
    String ruc_pro;
    String dv_pro;
    String representante_pro;
    String nombre_pro;
    String direccion_pro;
    String telefono_pro;
    String celular_pro;
    String correo_pro;

    public String getId_proveedor() {
        return id_proveedor;
    }

    public void setId_proveedor(String id_proveedor) {
        this.id_proveedor = id_proveedor;
    }

    public String getRuc_pro() {
        return ruc_pro;
    }

    public void setRuc_pro(String ruc_pro) {
        this.ruc_pro = ruc_pro;
    }

    public String getDv_pro() {
        return dv_pro;
    }

    public void setDv_pro(String dv_pro) {
        this.dv_pro = dv_pro;
    }

    public String getRepresentante_pro() {
        return representante_pro;
    }

    public void setRepresentante_pro(String representante_pro) {
        this.representante_pro = representante_pro;
    }

    public String getNombre_pro() {
        return nombre_pro;
    }

    public void setNombre_pro(String nombre_pro) {
        this.nombre_pro = nombre_pro;
    }

    public String getDireccion_pro() {
        return direccion_pro;
    }

    public void setDireccion_pro(String direccion_pro) {
        this.direccion_pro = direccion_pro;
    }

    public String getTelefono_pro() {
        return telefono_pro;
    }

    public void setTelefono_pro(String telefono_pro) {
        this.telefono_pro = telefono_pro;
    }

    public String getCelular_pro() {
        return celular_pro;
    }

    public void setCelular_pro(String celular_pro) {
        this.celular_pro = celular_pro;
    }

    public String getCorreo_pro() {
        return correo_pro;
    }

    public void setCorreo_pro(String correo_pro) {
        this.correo_pro = correo_pro;
    }

}
